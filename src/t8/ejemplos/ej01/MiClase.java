package t8.ejemplos.ej01;

public class MiClase {
	/**
	 * Dado un número, devuelve el doble del mismo
	 * @param numero el número del cual se quiere calcular el doble
	 * @return el doble del número proporcionado
	 */
	public int doble(int numero) {
		return 2*numero;
	}
	
	
	
	
	
	
	
	
	/**
	 * Dado un número, devuelve el triple del mismo
	 * @param numero el número del cual se quiere calcular el triple
	 * @return el triple del número proporcionado
	 */
	public int triple(int numero) {
		return 3*numero;
	}
	
}
